package chapter5;

public class Fuction {
	int i;
	int add(int num1,int num2) {
		return num1 + num2;
	}
	public int subtract(int num1,int num2) {
		return num1 - num2;
	}
	int multiply(int num1,int num2) {
		return num1 * num2;
	}
	int dividing(int num1,int num2) {
		return num1 / num2;
	}
	
}
